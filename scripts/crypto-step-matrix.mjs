#!/usr/bin/env node
/**
 * Generate docs/CRYPTO_TEST_MATRIX.md from shared/crypto-workflow-step-ids.js + step handler scans.
 *   node scripts/crypto-step-matrix.mjs --write   # update committed matrix
 *   node scripts/crypto-step-matrix.mjs --check   # exit 1 if matrix differs (CI)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const outPath = path.join(root, 'docs', 'CRYPTO_TEST_MATRIX.md');
const cwPath = path.join(root, 'shared', 'crypto-workflow-step-ids.js');

function parseCryptoStepIds() {
  const cw = fs.readFileSync(cwPath, 'utf8');
  const start = cw.indexOf('CRYPTO_OR_PULSE_STEP_TYPES = [');
  if (start < 0) throw new Error('CRYPTO_OR_PULSE_STEP_TYPES not found');
  const end = cw.indexOf('\n  ];', start);
  if (end < 0) throw new Error('end of CRYPTO_OR_PULSE_STEP_TYPES not found');
  const block = cw.slice(start, end);
  const types = [];
  const strRe = /'([a-zA-Z][a-zA-Z0-9_]*)'/g;
  let m;
  while ((m = strRe.exec(block)) !== null) types.push(m[1]);
  return types;
}

function firstCfsMessage(handlerPath) {
  if (!fs.existsSync(handlerPath)) return '—';
  const s = fs.readFileSync(handlerPath, 'utf8');
  const re = /type:\s*['"](CFS_[A-Z0-9_]+)['"]/g;
  const found = new Set();
  let mm;
  while ((mm = re.exec(s)) !== null) found.add(mm[1]);
  if (found.size === 0) return '—';
  return [...found].slice(0, 3).join(', ') + (found.size > 3 ? '…' : '');
}

function hasStepTests(stepId) {
  return fs.existsSync(path.join(root, 'steps', stepId, 'step-tests.js'));
}

function recommendLayers(stepId) {
  const id = stepId;
  if (id === 'selectFollowingAccount' || id === 'rugcheckToken') return 'L1 · L5';
  if (/^solanaWatch|^bscWatch|^watchActivity/.test(id)) return 'L1 · L2 · L5';
  if (/^bscQuery$/.test(id)) return 'L1 · L5';
  if (/^aster/.test(id)) return 'L1 · L2 · L5';
  if (/^bsc/.test(id)) return 'L1 · L2 · L3 · L5';
  if (/^solana|^raydium|^meteora/.test(id)) return 'L1 · L2 · L4 · L5';
  return 'L1 · L5';
}

function primaryNetwork(stepId) {
  if (/^bsc|^bscWatch/.test(stepId)) return 'BNB Chain (EVM)';
  if (/^solana|^raydium|^meteora|^solanaWatch/.test(stepId)) return 'Solana';
  if (/^aster/.test(stepId)) return 'Aster (HTTP)';
  if (/watchActivity|rugcheck|selectFollowing/.test(stepId)) return 'Pulse / APIs';
  return '—';
}

function noteFor(stepId) {
  if (/^bscPancake|^bscAggregator|^bscSellability/.test(stepId)) {
    return 'Fork: mainnet addresses in `bsc-evm.js`; testnet 97 needs address map.';
  }
  if (/raydium|meteora|solanaJupiter|solanaPump/.test(stepId)) {
    return 'Devnet pools/APIs differ; full path often mainnet-canary (L5).';
  }
  if (/^aster/.test(stepId)) return 'Signed HTTP; no local chain.';
  if (/Watch|watchActivity|rugcheck|bscQuery/.test(stepId)) return 'Indexers/API keys; not covered by RPC smoke alone.';
  return '';
}

function buildMarkdown(ids) {
  const rows = ids.map((id) => {
    const handler = path.join(root, 'steps', id, 'handler.js');
    const cfs = firstCfsMessage(handler);
    const tests = hasStepTests(id) ? 'yes' : 'no';
    const note = noteFor(id);
    return `| \`${id}\` | ${primaryNetwork(id)} | ${cfs} | ${recommendLayers(id)} | ${tests} | ${note || '—'} |`;
  });

  const header = `<!-- AUTO-GENERATED by scripts/crypto-step-matrix.mjs — do not edit by hand; run npm run report:crypto-matrix -->
# Crypto / Pulse step test matrix

This table lists steps gated as **crypto or Pulse** (\`shared/crypto-workflow-step-ids.js\`). It is **regenerated**; CI fails if it drifts (\`npm run verify:crypto-matrix\`).

## Test layers (summary)

| Layer | What runs | Where |
|-------|-----------|--------|
| **L1** | Message shapes, pure logic, \`step-tests.js\`, unit tests | Always in CI (\`npm run test:unit\`, step tests) |
| **L2** | Read-only RPC (\`getHealth\`, \`getSlot\`, \`eth_chainId\`, \`eth_blockNumber\`; fork job: \`eth_getCode\` V2 router on 56, Infinity Vault Chapel on 97) | Optional secrets: \`npm run test:crypto-rpc-smoke\` — see \`docs/CRYPTO_CI_SMOKE.md\` |
| **L3** | BSC-shaped EVM fork (Anvil) + golden-path txs | Local/self-hosted: \`docker compose\` + \`npm run test:crypto-evm-fork-smoke\` |
| **L4** | Solana devnet (wallet + subset of steps) | Manual / staging; \`cluster: devnet\` + faucet |
| **L5** | Mainnet/API canary (tiny amount or read-only API) | Manual or scheduled; Aster, BscScan, Jupiter, etc. |

**Not all rows are fully testable on L3/L4** — see Notes.

## Matrix

| Step | Network | Service worker message(s) | Layers | \`step-tests.js\` | Notes |
|------|---------|----------------------------|--------|-------------------|-------|
${rows.join('\n')}

## Regenerate

\`\`\`bash
npm run report:crypto-matrix
npm run verify:crypto-matrix   # CI
\`\`\`
`;
  return header;
}

const args = process.argv.slice(2);
const mode = args.includes('--check') ? 'check' : args.includes('--write') ? 'write' : 'stdout';

const ids = parseCryptoStepIds();
const md = buildMarkdown(ids);

if (mode === 'stdout') {
  process.stdout.write(md);
  process.exit(0);
}

if (mode === 'write') {
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, md, 'utf8');
  console.log('Wrote', path.relative(root, outPath));
  process.exit(0);
}

// --check
const existing = fs.existsSync(outPath) ? fs.readFileSync(outPath, 'utf8') : '';
if (existing === md) {
  console.log('verify:crypto-matrix: OK (up to date)');
  process.exit(0);
}
console.error('verify:crypto-matrix: docs/CRYPTO_TEST_MATRIX.md is stale. Run: npm run report:crypto-matrix');
process.exit(1);
